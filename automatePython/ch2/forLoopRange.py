# This demonstrates the for loop range options
print("Range starting at 12 and ending at 16")
for i in range(12, 17):
    print(i)

print("Range starting at 0 and ending at but not including 10 by 2s")
for i in range(0, 10, 2):
    print(i)

print("Range counting down from 25 by 1")
for i in range(26, -1, -1):
    print(i)
